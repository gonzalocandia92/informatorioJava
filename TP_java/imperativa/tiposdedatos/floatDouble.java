package imperativa.tiposdedatos;

public class floatDouble {

    public static void main(String[] args) {

        // Float tiene 32 bits
        float minValueFloat = Float.MIN_VALUE;
        float maxValueFloat = Float.MAX_VALUE;
        System.out.println("Valor Float Min: " + minValueFloat + " Max: " + maxValueFloat);

        // Double tiene 64 bits
        double minValueDouble = Double.MIN_VALUE;
        double maxValueDouble = Double.MAX_VALUE;
        System.out.println("Valor Double Min: " + minValueDouble + " Max: " + maxValueDouble);


    }

}
