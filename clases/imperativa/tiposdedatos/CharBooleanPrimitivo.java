package imperativa.tiposdedatos;

public class CharBooleanPrimitivo {

    public static void main(String[] args) {

        // Char es caracter: a, b, c, d, e...
        char unChar = 'a';
        char unCharConUnicode = '\u0985';
        System.out.println("Un char con Unicode: " + unCharConUnicode);

        // Booleans: true; false;
        boolean miVerdadero = true;
        boolean miFalso = false;
        System.out.println("Mi verdadero: " + miVerdadero + " - Mi Falso: " + miFalso);

    }

}
